# Desafio: Conte quantas vezes um elemento específico aparece em uma lista ['a', 'b', 'c', 'a', 'b', 'a']. Dica: Use o método count() da lista.

elementos = ['a', 'b', 'c', 'a', 'b', 'a']
elemento1 = 'a'
elemento2 = 'b'
elemento3 = 'c'

print(f'Contamos {elementos.count(elemento1)} vezes o elemento: {elemento1}')
print(f'Contamos {elementos.count(elemento2)} vezes o elemento: {elemento2}')
print(f'Contamos {elementos.count(elemento3)} vezes o elemento: {elemento3}')