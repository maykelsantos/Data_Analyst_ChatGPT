# Desafio: Calcule a média dos elementos de uma lista de números [10, 20, 30, 40, 50]. Dica: A média é a soma dos elementos dividida pelo número de elementos.

lista_numeros = [10, 20, 30, 40, 50]

media_valores_lista = sum(lista_numeros) / 5
print(f'A média dos valores desta lista é {media_valores_lista:.2f}')

