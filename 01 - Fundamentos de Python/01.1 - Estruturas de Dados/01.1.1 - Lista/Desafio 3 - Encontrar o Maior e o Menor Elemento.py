# Desafio: Encontre o maior e o menor elemento em uma lista [5, 2, 9, 1, 7, 6]. Dica: Use as funções max() e min().

lista_numeros = [5, 2, 9, 1, 7, 6]
maior_numero_lista = max(lista_numeros)
menor_numero_lista = min(lista_numeros)

print(f'O maior número desta lista é {maior_numero_lista}')
print(f'O menor número desta lista é {menor_numero_lista}')