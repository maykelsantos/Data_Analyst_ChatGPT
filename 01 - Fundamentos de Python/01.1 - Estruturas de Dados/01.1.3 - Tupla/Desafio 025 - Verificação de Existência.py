# Tarefa: Verifique se o número 4 está presente na tupla tupla3.
# Dica: Use o operador in para verificar a existência de um elemento em uma tupla.

tupla3 = (1, 2, 3, 4, 5, 6)

if 6 in tupla3 == True:
    print('O número 6 está presente na tupla3!')
else:
    print('O número 6 não está presente na tupla3!')